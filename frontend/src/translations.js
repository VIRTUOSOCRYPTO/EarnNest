export const getTranslation = (category, key, language = 'en') => {
  try {
    return translations[category][language][key] || translations[category]['en'][key] || key;
  } catch (error) {
    return key;
  }
};

export const formatCurrency = (amount, language = 'en') => {
  const formatter = new Intl.NumberFormat(language === 'hi' ? 'hi-IN' : language === 'ta' ? 'ta-IN' : 'en-IN', {
    style: 'currency',
    currency: 'INR',
    minimumFractionDigits: 0,
    maximumFractionDigits: 0
  });
  return formatter.format(amount);
};

